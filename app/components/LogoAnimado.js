export default function LogoAnimado() {
  const pathname = usePathname();
  const [phase, setPhase] = useState('idle'); // 'idle' | 'drawing' | 'settling'
  const svgRef = useRef(null);

  // Decide, uma única vez por sessão, se o traçado deve tocar. Roda em
  // useLayoutEffect (não useEffect) para poder disparar o setPhase antes
  // do browser pintar — evita qualquer decisão tardia após o primeiro frame.
  useLayoutEffect(() => {
    if (pathname !== '/') return undefined;

    // A partir daqui quem controla a visibilidade do PNG é o React (fases
    // drawing/settling/idle) — remove o gate do script inline de
    // app/layout.js, que só existe para cobrir o frame pré-hidratação.
    document.documentElement.classList.remove('logo-animar');

    let animar = true;
    try {
      if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
        animar = false;
      }
      if (animar && sessionStorage.getItem(SESSION_KEY)) {
        animar = false;
      }
    } catch {
      animar = false;
    }

    if (!animar) return undefined;

    try {
      sessionStorage.setItem(SESSION_KEY, '1');
    } catch {
      // sessionStorage indisponível (ex.: modo privado restrito) — segue
      // e anima mesmo assim, só não persiste o flag entre navegações.
    }

    setPhase('drawing');
    return undefined;
  }, [pathname]);

  // Prepara o estado inicial "invisível" de cada traço de forma síncrona
  // (antes do browser pintar), para nunca dar um flash da wordmark cheia
  // antes do traçado começar. Só então agenda a sequência de desenho.
  useLayoutEffect(() => {
    if (phase !== 'drawing') return undefined;
    const svg = svgRef.current;
    if (!svg) return undefined;

    const paths = Array.from(svg.querySelectorAll('path'));
    const timers = [];
    let decorrido = 0;

    paths.forEach((path) => {
      const comprimento = path.getTotalLength();
      const duracao = Math.max(MIN_DURATION, (comprimento / SPEED) * 1000);

      path.style.transition = 'none';
      path.style.strokeDasharray = String(comprimento);
      path.style.strokeDashoffset = String(comprimento);

      const atraso = decorrido;
      timers.push(
        setTimeout(() => {
          path.getBoundingClientRect(); // força reflow antes de habilitar a transição
          path.style.transition = `stroke-dashoffset ${duracao}ms ease-out`;
          path.style.strokeDashoffset = '0';
        }, atraso)
      );

      decorrido += duracao;
    });

    timers.push(setTimeout(() => setPhase('settling'), decorrido));

    return () => timers.forEach(clearTimeout);
  }, [phase]);

  // Após o crossfade, encerra em 'idle' (só o PNG estático, igual ao visual atual).
  useEffect(() => {
    if (phase !== 'settling') return undefined;
    const timer = setTimeout(() => setPhase('idle'), SETTLE_DURATION);
    return () => clearTimeout(timer);
  }, [phase]);

  const baseClasses = [
    styles.base,
    phase === 'drawing' ? styles.baseHidden : '',
    phase === 'settling' ? styles.baseSettling : '',
  ]
    .filter(Boolean)
    .join(' ');

  return (
    <span className={styles.wrap}>
      <Image
        src="/marca/logo-simplicitude-677w.png"
        alt="Simplicitude"
        width={677}
        height={118}
        sizes="(max-width: 1024px) 30.7vw, 30.7vw"
        className={baseClasses}
        data-logo-base
        priority
      />
      {phase !== 'idle' && (
        <svg
          ref={svgRef}
          className={`${styles.svg} ${phase === 'settling' ? styles.svgSettling : ''}`}
          viewBox="0 0 172.18108 31.367828"
          aria-hidden="true"
        >
          <g transform="translate(-20.132945,-94.859376)">
            {STROKES.map((d) => (
              <path
                key={d}
                d={d}
                fill="none"
                stroke="#000000"
                strokeWidth="1.22129"
                strokeLinecap="round"
                strokeLinejoin="round"
              />
            ))}
          </g>
        </svg>
      )}
    </span>
  );
}
